    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jtxtSaldo.getText().isEmpty()&&!jtxtNumeroDeCuenta.getText().isEmpty()&&!jComboBox1.getSelectedItem().equals("...")) {

            Date date = Calendar.getInstance().getTime();
            DateFormat dateFormat = new SimpleDateFormat("yyyy-mm-dd hh:mm:ss");
            String strDate = dateFormat.format(date);
            String saldostr = this.jtxtSaldo.getText();
            int saldo = Integer.parseInt(saldostr);
            String nombres = (String) jComboBox1.getSelectedItem();
            Cliente cliente = new Cliente(nombres);

            CuentaTotalDAO cuentaTotal = new CuentaTotalDAO();
            String numeroCuenta = this.jtxtNumeroDeCuenta.getText();
            Cuenta cuenta = new Cuenta(cuentaTotal.selectID(clienteDAO.selectID(cliente)), numeroCuenta, strDate, saldo);
            
            cuentaDAO.guardarSaldo(cuenta);
            CuentaTotalDAO c = new CuentaTotalDAO();
            c.update(cuenta);
            JOptionPane.showMessageDialog(this, "La cuenta se guardó correctamente", "Aceptado", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Rellene todos los espacios", "Problema", JOptionPane.ERROR_MESSAGE);

        }
        this.jComboBox1.setSelectedItem("...");
        this.jtxtSaldo.setText("");
        this.jtxtNumeroDeCuenta.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

